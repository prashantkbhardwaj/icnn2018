               <p>
                   It is a great pleasure to announce that 5<sup>th</sup> International Conference of Nano-materials and Nano-composites (ICNN) will be held at VIT University, Chennai during 08-10 February, 2018. 
                
                    It is very exciting to see nano-images of materials and their possible immense applications in the daily science column of newspapers. Every month numerous research papers are being published on potential nano-materials and their associated applications for the benefit of the society. Looking at the diversified research findings, it appears that in almost all the fields of materials research, nanostructures are bringing innovation and technological applications. 5<sup>th</sup> ICNN, therefore seeks a conglomeration of researchers and experts from academia and industries across the globe, to exchange ideas and intricacies of technological advances in nano-world. Experimental and theoretical aspects on nanoparticles/composites, CNT, graphene and other nanostructures will be addressed in this forum. Special emphasis will be given to interdisciplinary areas of health, energy and environment—their challenges and success stories.
                </p>
